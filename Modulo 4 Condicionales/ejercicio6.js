function filosofoHipster(profesion, nacionalidad,kilometros){
    return profesion == "Músico" && nacionalidad == "Argentina" && kilometros >=2;
}

console.log(filosofoHipster("Ingeniero","Mexico",5));
console.log(filosofoHipster("Músico","Argentina",6));
console.log(filosofoHipster("Programador","España",1));
